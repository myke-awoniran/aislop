const prettifyFallback = (ruleId: string): string => {
	const tail = ruleId.includes("/") ? ruleId.slice(ruleId.indexOf("/") + 1) : ruleId;
	const spaced = tail.replace(/[-_]/g, " ").replace(/\//g, " · ");
	return spaced.charAt(0).toUpperCase() + spaced.slice(1);
};

export const labelForRule = (ruleId: string): string =>
	RULE_LABELS[ruleId] ?? prettifyFallback(ruleId);
